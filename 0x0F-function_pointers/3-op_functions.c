#include "3-calc.h"

/**
 * op_add - calculate the sum of two intigers
 * @a: first intiger
 * @b: second intiger
 *
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates the subtraction of two intigers
 * @a: first intiger
 * @b: second intiger
 *
 * Return: difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates the multiplication ot two intigers
 * @a: first intiger
 * @b: second intiger
 *
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates the division of two intigers
 * @a: first intiger
 * @b: second intiger
 *
 * Return:  the division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates the module of two number
 * @a: first intiger
 * @b: second intiger
 *
 * Return: the module of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
